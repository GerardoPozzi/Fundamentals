# igual_mayor_menor.py
# pide un número e imprime si es igual, mayor o menor que 0

num = int(input('Ingresá un número cualquiera: '))

if num == 0:
    print('El número ingresado es 0.')

elif num > 0:
    print('El número ingresado es mayor que 0.')

else:
    print('El número ingresado es menor que 0.')
